#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nb;
	char	digite;

	nb = (long)n;
	if (nb < 0)
	{
		nb *= -1;
		write(fd, "-", 1);
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		digite = nb + 48;
		write(fd, &digite, 1);
	}
}
